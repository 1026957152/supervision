    public static SuretyTypeStatusEnum fromString(String text) {
        for (SuretyTypeStatusEnum status : SuretyTypeStatusEnum.values()) {
            if (status.getText().equals(text) ) {
                return status;
            }
        }
        throw new RuntimeException("no customer status " + text);
    }

    public static SuretyTypeStatusEnum fromInt(Integer text) {
        for (SuretyTypeStatusEnum status : SuretyTypeStatusEnum.values()) {
            if (status.getId().equals(text) ) {
                return status;
            }
        }
        throw new RuntimeException("no customer status " + text);
    }

