    public static LoanSubAccountTypeEnum fromString(String text) {
        for (LoanSubAccountTypeEnum status : LoanSubAccountTypeEnum.values()) {
            if (status.getText().equals(text) ) {
                return status;
            }
        }
        throw new RuntimeException("no customer status " + text);
    }

    public static LoanSubAccountTypeEnum fromInt(Integer text) {
        for (LoanSubAccountTypeEnum status : LoanSubAccountTypeEnum.values()) {
            if (status.getId().equals(text) ) {
                return status;
            }
        }
        throw new RuntimeException("no customer status " + text);
    }

